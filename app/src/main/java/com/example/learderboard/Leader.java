package com.example.learderboard;

public class Leader {
    public String id;
    public String name;
    public String score;
    public String country;
    public String badgeUrl;

    public Leader(String name, String score,String country,String badgeUrl) {
        this.id = id;
        this.name = name;
        this.score = score;
        this.country=country;
        this.badgeUrl=badgeUrl;
    }
}
